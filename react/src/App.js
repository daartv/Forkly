import {
  BrowserRouter as Router,
  Route,
  Link
} from 'react-router-dom'

class App extends Component {
  constructor (props) {
    super(props)
    this.state = {
      username: null,
      currentRecipe: []
    }
    this.logout = this.logout.bind(this)
  }

  componentDidMount () {
    this.getUsername()
  }

  getUsername () {
    var context = this

    $.ajax({
      url: '/username',
      type: 'GET',
      contentType: 'application/json',
      success: function (data) {
        context.setState({username: data})
      },
      error: function (err) {
        throw err
      }
    })
  }

  logout () {
    var Appcontext = this

    $.ajax({
      url: '/logout',
      type: 'GET',
      contentType: 'application/json',
      success: function (data) {
        Appcontext.getUsername()
      },
      error: function (err) {
        throw err
      }
    })
  }
  render () {
    return (
      <SearchRecipes />
    )
  }
}

export default App

